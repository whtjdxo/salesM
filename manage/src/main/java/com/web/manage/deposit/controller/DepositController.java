    @RequestMapping(value = "processDeposit", method = RequestMethod.POST)
    public @ResponseBody ReturnDataVO processDeposit(@RequestBody HashMap<String, Object> hashmapParam) {
        ReturnDataVO result = new ReturnDataVO();
        try {
            boolean success = depositService.processDeposit(hashmapParam);
            if (success) {
                result.setResultCode("S000");
                result.setResultMsg("Deposit processed successfully.");
            } else {
                result.setResultCode("F000");
                result.setResultMsg("Deposit processing failed.");
            }
        } catch (Exception e) {
            result.setResultCode("F500");
            result.setResultMsg("An error occurred while processing the deposit.");
            e.printStackTrace();
        }
        return result;
    }
